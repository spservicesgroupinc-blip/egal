// --- FILE MANAGER COMPONENT ---
const FileManager: React.FC<{ files: CaseFile[]; onUpload: (f: CaseFile) => void; onDelete: (id: string) => void }> = ({ files, onUpload, onDelete }) => {
  const handleFileChange = async (e: React.ChangeEvent<HTMLInputElement>) => {
    const file = e.target.files?.[0];
    if (!file) return;

    let content = "";
    let inlineData = undefined;

    // Handle Binary Files (PDF, Images)
    if (file.type === 'application/pdf' || file.type.startsWith('image/')) {
        try {
            const base64 = await new Promise<string>((resolve, reject) => {
                const reader = new FileReader();
                reader.onload = () => {
                    const result = reader.result as string;
                    // remove "data:application/pdf;base64," prefix
                    const b64 = result.split(',')[1]; 
                    resolve(b64);
                };
                reader.onerror = reject;
                reader.readAsDataURL(file);
            });
            
            content = `[Binary File: ${file.type}]`;
            inlineData = {
                mimeType: file.type,
                data: base64
            };
        } catch (error) {
            alert("Error reading file.");
            return;
        }
    } else {
        // Fallback for text-based files
        content = await file.text();
    }

    const newFile: CaseFile = {
        id: Date.now().toString(),
        name: file.name,
        type: file.type,
        content: content,
        inlineData: inlineData,
        dateAdded: Date.now(),
    };
    onUpload(newFile);
  };

  return (
    <div className="p-8 max-w-5xl mx-auto h-full overflow-y-auto legal-scroll">
      <div className="flex justify-between items-center mb-8">
        <div>
            <h2 className="text-3xl font-serif font-bold text-legal-900">Case Files</h2>
            <p className="text-gray-500 mt-2">Upload motions, orders, and evidence (PDF, Text, Images). These files are saved locally and analyzed by AI.</p>
        </div>
        <label className="flex items-center gap-2 bg-legal-800 hover:bg-legal-700 text-white px-4 py-2 rounded-lg cursor-pointer transition shadow-lg">
          <Icons.Upload />
          <span>Upload Document</span>
          <input 
            type="file" 
            className="hidden" 
            onChange={handleFileChange} 
            accept=".txt,.md,.json,.csv,.pdf,.jpg,.jpeg,.png" 
          />
        </label>
      </div>

      <div className="grid grid-cols-1 md:grid-cols-2 lg:grid-cols-3 gap-6">
        {files.length === 0 && (
            <div className="col-span-full text-center py-20 bg-white rounded-xl border-2 border-dashed border-gray-300">
                <Icons.Document />
                <p className="text-gray-500 mt-4">No files stored. Upload documents to give the AI context.</p>
            </div>
        )}
        {files.map(file => (
          <div key={file.id} className="bg-white p-6 rounded-xl shadow-sm border border-gray-100 hover:shadow-md transition relative group">
            <div className="flex items-start justify-between mb-4">
              <div className="p-3 bg-legal-50 text-legal-600 rounded-lg">
                <Icons.Document />
              </div>
              <button 
                onClick={() => onDelete(file.id)} 
                className="text-gray-300 hover:text-red-500 transition-colors"
                title="Delete File"
              >
                  <svg xmlns="http://www.w3.org/2000/svg" fill="none" viewBox="0 0 24 24" strokeWidth={1.5} stroke="currentColor" className="w-5 h-5">
                    <path strokeLinecap="round" strokeLinejoin="round" d="M14.74 9l-.346 9m-4.788 0L9.26 9m9.968-3.21c.342.052.682.107 1.022.166m-1.022-.165L18.16 19.673a2.25 2.25 0 01-2.244 2.077H8.084a2.25 2.25 0 01-2.244-2.077L4.772 5.79m14.456 0a48.108 48.108 0 00-3.478-.397m-12 .562c.34-.059.68-.114 1.022-.165m0 0a48.11 48.11 0 013.478-.397m7.5 0v-.916c0-1.18-.91-2.164-2.09-2.201a51.964 51.964 0 00-3.32 0c-1.18.037-2.09 1.022-2.09 2.201v.916m7.5 0a48.667 48.667 0 00-7.5 0" />
                  </svg>
              </button>
            </div>
            <h3 className="font-semibold text-legal-900 truncate" title={file.name}>{file.name}</h3>
            <p className="text-xs text-gray-500 mt-1">Added: {new Date(file.dateAdded).toLocaleDateString()}</p>
            <div className="mt-4 pt-4 border-t border-gray-100">
               {file.inlineData ? (
                   <div className="flex items-center gap-2 text-xs text-legal-600 bg-gray-50 p-2 rounded">
                       <span className="font-bold uppercase">{file.type.split('/')[1]} Document</span>
                       <span className="text-gray-400">â€¢ Ready for AI</span>
                   </div>
               ) : (
                   <p className="text-xs text-gray-400 line-clamp-3 font-mono bg-gray-50 p-2 rounded break-all">{file.content.substring(0, 150)}</p>
               )}
            </div>
          </div>
        ))}
      </div>
    </div>
  );
};

