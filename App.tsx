                <label className="block text-sm font-semibold text-gray-700 mb-1">Specific Facts/Instructions</label>
                <textarea 
                    className="w-full p-2 border border-gray-300 rounded focus:border-legal-500 outline-none h-40" 
                    placeholder="Enter key facts (dates, names, events) to be included in the motion..."
                    value={instructions}
